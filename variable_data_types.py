# Python supports integers, float and complex numbers. (Complex numbers later)
MyInt=10
print (MyInt)

MyFloat=10.1
print (MyFloat)

# Other way to declare a float

MyOtherFloat = float(10.1)
print (MyOtherFloat)

# Let us see string support

MyString = "Python program"
print (MyString)

# Note the use of double quotes to wrap the string.  If one uses
# single quote and if there is a word with apostrophe, it will
# terminate the string

# Basic arithmatic operations on variables
one = 1
two = 2
three = one + two
print (three)

# Concat operations on strings
string1 = "Python"
string2 = "Program"
